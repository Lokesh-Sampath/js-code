//Arrays

// it works as normal array and its functions

const arr = new Array(1,2,3,4);
console.log(arr);

console.log(arr.push(5)); //adds at last
console.log(arr.pop()); //removes the last

console.log(arr.unshift(0));  // adds at first
console.log(arr.shift());  //removes at last

console.log(arr.includes(4)); //returns in true or false
console.log(arr.indexOf(3)); // returns the index of that integer

